
public class Grade {
   private int mark;
   private String name;
   
   
public Grade(int mark, String name) {
	super();
	this.mark = mark;
	this.name = name;
}
public int getMark() {
	return mark;
}
public void setMark(int mark) {
	this.mark = mark;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
   
   
}
